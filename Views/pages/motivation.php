<h4>Красивая форма</h4>


<p>Сначала купите спортивную форму. Конечно, нужно выбирать одежду, изготовленную из натуральных материалов, тщательно подбирая вой размер, 
    чтобы ощущать максимальное удобство. Покупая спортивную форму, вы переживаете и психологический момент. Вы представляете, как красиво будет 
    смотреться на вас эта форма, особенно через полгода занятий, когда фигура изменится в лучшую сторону и вы станете похожи на те спортсменов, 
    которые изображены на плакате с рекламой этой самой формы! Это весьма помогает почувствовать человеком, близким к спорту, даже если это пока не так.</p>

<h4>Когда тренироваться?</h4>

<p>Специалисты рекомендуют это делать до ужина или завтрака, то есть до еды, но не полностью на голодный желудок. Съешьте пред тренировкой 
    что-нибудь лёгкое – салат, йогурт, сок или яблоко. Не ешьте ничего один час после занятий. Зато воду пить нужно и во время занятий, и после них.

    Для достижения своей цели, чтобы не возникло отвращения к спорту и вам не захотелось отказаться от него, самое главное - не получить перегрузок 
    на начальном этапе. Нагружайте себя постепенно.</p>

<h4>Нагружайтесь постепенно</h4>


<p>Только небольшая и даже приятная нагрузка может стать для вас привычкой, которая будет развиваться с течением времени. Ваша цель должна быть
    конкретной. Вы должны разобраться - что делать, сколько и каким образом. Метод «надо работать, но как - я не знаю», не работает.</p>

<h4>Фиксируйте успехи</h4>


<p>Ваши достижения должны быть видны визуально, чтобы выяснить, была ли вами достигнута цель дня. Например, поставьте себе цель каждый день в течение
    недели проводить тридцать минут на велотренажере, и не отступайте от нее ни в коем случае. Запланируйте цель на долгосрочный период, достигая все
    больших успехов. Записывайте результаты ваших достижений и сравнивайте их время от времени - это будет стимулировать новые успехи.
    Объясните друзьям и коллегам, а также в социальных сетях, что такое физические упражнения и какая от них польза, расскажите о своих собственных 
    достижениях. Если все вокруг вас будут знать, что вы занимаетесь спортом, и будут ждать очередного результата, ваши старания вернутся к вам новыми 
    успехами.</p>
