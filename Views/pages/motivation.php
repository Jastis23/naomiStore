
<h4>Мотивация к спорту</h4>
<p>Довольно трудно начинать заниматься физическими упражнениями, если вы ранее этого никогда не делали. Многие неискушенные 
    новички то начинают тренироваться, то оставляют занятия, а затем пытаются начать снова...</p>
<img src="public/images/arnold.jpg"/>
<p>Одной из основных проблем в данном случае является то, что это слишком сложно для людей, которые не привыкли проявлять
    силу воли. Вы начинаете с большим энтузиазмом заниматься спортом, ставите перед собой большие цели, одна из них заключается 
    в тренировках один час в день, и вдруг вы понимаете, что для вас это слишком сложно.</p>

<h4>Не ставьте невыполнимых целей</h4>

<p>Иногда человек намечает слишком много целей. В один прекрасный день вдруг становится понятно, что вы хотите заниматься спортом, чтобы похудеть и 
    красиво выглядеть, ходить в солярий, чтобы ваша кожа имела красивый тон загара, соблюдать диету, при этом правильно питаться и успевать бегать
    на танцы. Но времени катастрофически не хватает. Все нагрузки нужно брать на себя постепенно.</p>

<h4>Мотивация</h4>


<p>Отсутствие мотивации также является большой проблемой. Лучшие виды спорта – коллективные. В конце концов, если уж вы решили, что должны 
    начать тренироваться, то лучше сделайте это с друзьями. В спортивных секциях чувствуется особая атмосфера принадлежности к спорту, здоровой 
    жизни, молодости и красоте. Этот фактор многих привлекает, и это прекрасно.</p>
